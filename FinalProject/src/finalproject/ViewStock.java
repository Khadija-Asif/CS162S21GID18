    public void showDataInTable() {
        tbModel = (DefaultTableModel) jTable1.getModel();
        tbModel.setRowCount(0);
        for (int i = 0; i < Customer_Director.prod.size(); i++) {
            Object[] obj = new Object[5];
            obj[0] = Customer_Director.prod.get(i).getProductID();
            obj[1] = Customer_Director.prod.get(i).getProductName();
            obj[2] = Customer_Director.prod.get(i).getProductQuantity();
            obj[3] = Customer_Director.prod.get(i).getProductPrice();
            obj[4] = Customer_Director.prod.get(i).getProductType();

            tbModel.addRow(obj);
        }
        //for sales table
        tbModel = (DefaultTableModel) jTable2.getModel();
        tbModel.setRowCount(0);
        for (int i = 0; i < ManagingSystem.SalesList.size(); i++) {
            Object[] obj = new Object[7];
            obj[0] = ManagingSystem.SalesList.get(i).getProductID();
            obj[1] = ManagingSystem.SalesList.get(i).getProductName();
            obj[2] = ManagingSystem.SalesList.get(i).getProductQuantity();
            obj[3] = ((Sales)ManagingSystem.SalesList.get(i)).getNewPrice();
            obj[4] = ManagingSystem.SalesList.get(i).getProductType();
            obj[5] = ((Sales)ManagingSystem.SalesList.get(i)).getSalesPercentage();
            obj[6] = ((Sales)ManagingSystem.SalesList.get(i)).getSalesTimeDuration();
            
            tbModel.addRow(obj);
        }
    }

